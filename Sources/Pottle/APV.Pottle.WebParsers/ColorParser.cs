        public static readonly int MaxColorWords = Colors.Keys.Max(color => color.Split(new[] { " ", "-" }, StringSplitOptions.RemoveEmptyEntries).Length);

        public static string Parse(string sentence, out bool onlyColor)
        {
            onlyColor = false;
            if (!string.IsNullOrWhiteSpace(sentence))
            {
                string lowerSentence = sentence.ToLowerInvariant();
                string[] words = lowerSentence.Split(new[] { " ", "-", "+", ",", ";", ":", "!", "/", "\\" }, StringSplitOptions.RemoveEmptyEntries);
                words = words.Select(word => word.Trim()).Where(word => !string.IsNullOrEmpty(word)).ToArray();
                int length = words.Length;
                if (length > 0)
                {
                    int maxWords = (length >= MaxColorWords) ? MaxColorWords : length;
                    for (int i = maxWords; i >= 1; i--)
                    {
                        string colorWord = string.Join(" ", words.Take(i));
                        int index = Colors.IndexOfKey(colorWord);
                        if (index != -1)
                        {
                            onlyColor = (i == length);
                            string color = Colors.Values[index];
                            return color;
                        }
                    }
                }
            }
            return null;
        }

        public static string Parse(string sentence)
        {
            bool onlyColor;
            return Parse(sentence, out onlyColor);
        }
